/**
 * Publish-from-source type build for `@vc-frontend/core` (VCST-5159, #3).
 *
 * Produces a SINGLE self-contained `dist/index.d.ts` that a federated plugin can
 * type-check against without ever reaching into host source. This is the type
 * half of the "publish-from-source" contract: the running implementation is still
 * injected by the host at runtime via the MF shared singleton (see vite.config.ts
 * › MF_SHARED and client-app/modules/federated), so there is no runtime artifact —
 * only the declaration file that pins the plugin's compile-time contract.
 *
 * Why a two-step build:
 *   1. vue-tsc emits the facade's reachable type graph (~700 .d.ts, including the
 *      Vue SFC component types) into `.types-build/`. Raw, these still reference
 *      `@/…` host paths — useless to an external consumer.
 *   2. rollup-plugin-dts inlines that graph into one file, resolving `@/…` against
 *      the emitted tree and dropping every host-internal reference. What remains
 *      are imports from the shared peer packages (vue, vue-router, vue-i18n,
 *      @apollo/client, @vueuse/*) — which the plugin already has.
 *
 * Regenerate after ANY change to the facade surface (index.ts) or the types it
 * re-exports:  `yarn build:core-types`  (also runnable from repo root).
 * The output is committed so plugins type-check without a prebuild step; a CI
 * check should re-run this and fail on drift (documented as a follow-up).
 */
import { spawnSync } from "node:child_process";
import { existsSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { rollup } from "rollup";
import dts from "rollup-plugin-dts";

const CORE_API_DIR = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(CORE_API_DIR, "../..");
const EMIT_DIR = resolve(CORE_API_DIR, ".types-build");
const DIST_DIR = resolve(CORE_API_DIR, "dist");
const EMIT_ENTRY = resolve(EMIT_DIR, "core-api/index.d.ts");
const OUT_FILE = resolve(DIST_DIR, "index.d.ts");

const BANNER = `// AUTO-GENERATED by client-app/core-api/build-types.mjs — DO NOT EDIT.
// Self-contained public type contract of @vc-frontend/core (VCST-5159).
// Regenerate with: yarn build:core-types
`;

function step(msg) {
  console.log(`[build-types] ${msg}`);
}

// 1 ── emit the facade's declaration graph ────────────────────────────────────
step("emitting declarations with vue-tsc…");
rmSync(EMIT_DIR, { recursive: true, force: true });
const emit = spawnSync("npx", ["vue-tsc", "--project", resolve(CORE_API_DIR, "tsconfig.types.json")], {
  cwd: REPO_ROOT,
  encoding: "utf8",
  shell: process.platform === "win32",
});
// vue-tsc reports diagnostics in host files that are outside the facade surface
// (e.g. ui-kit components relying on ambient globals we don't load here). Those
// files never reach the rolled-up contract, so the emit is validated by its
// OUTPUT, not by exit code — declarations are still written (noEmitOnError:false).
if (!existsSync(EMIT_ENTRY)) {
  console.error(`[build-types] FAILED: no declarations emitted at ${EMIT_ENTRY} (vue-tsc exit ${emit.status}).`);
  console.error((emit.stdout ?? "") + (emit.stderr ?? ""));
  process.exit(1);
}

// 2 ── roll the graph up into one self-contained file ─────────────────────────
step("bundling into a single self-contained index.d.ts…");
const bundle = await rollup({
  input: EMIT_ENTRY,
  // Unresolved imports here are the shared peer packages (vue, @apollo/client, …) —
  // intentionally kept external; silence the expected noise.
  onwarn: () => {},
  plugins: [
    dts({
      respectExternal: false,
      compilerOptions: { baseUrl: EMIT_DIR, paths: { "@/*": [`${EMIT_DIR}/*`] } },
    }),
  ],
});
const { output } = await bundle.generate({ format: "es" });
await bundle.close();

let code = output[0].code;

// Guard: the whole point is zero host coupling. If any `@/…` survived, the rollup
// failed to inline something — fail loudly rather than ship a broken contract.
if (code.includes("@/")) {
  console.error("[build-types] FAILED: unresolved '@/' reference(s) remain in the rolled contract.");
  process.exit(1);
}

mkdirSync(DIST_DIR, { recursive: true });
writeFileSync(OUT_FILE, BANNER + "\n" + code, "utf8");
rmSync(EMIT_DIR, { recursive: true, force: true });

// External peer imports the contract expects the plugin (or host) to provide.
const externals = [...new Set([...code.matchAll(/from ['"]([^'".][^'"]*)['"]/g)].map((m) => m[1]))].sort();
step(`wrote ${OUT_FILE} (${code.split("\n").length} lines).`);
step(`external peer imports: ${externals.join(", ") || "(none)"}`);
